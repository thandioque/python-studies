first_int_number = int(input('Enter a first integer number: '))
second_int_number = int(input('Enter a second integer number: ')) 

if first_int_number > second_int_number :
   print('The first number is greater than the second')
elif second_int_number > first_int_number :
   print('The second number is greater than the first')
elif first_int_number == second_int_number :
   print("There isn't a greater number ! Numbers are the same.")
else: 
    print('Another conditional found !')




