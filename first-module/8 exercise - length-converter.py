meter = float(input('Enter the value in meters: '))
kilometers = meter / 1000
hectometers = meter / 100
dekameteres = meter / 10
decimeters = meter * 10
centimeters = meter * 100
millimeters = meter * 1000
print('The value in meters is: {}m \nThe value in kilometers is: {}km \nThe value in hectometers is: {}hm \nThe value in dekameteres is: {}dam'.format(meter, kilometers, hectometers, dekameteres))
print('The value in decimeters is: {}dm \nThe value in centimeters is: {}cm \nThe value in millimeters is: {}mm'.format(decimeters, centimeters, millimeters))
