something = input('Type something: ')

print('The primitive type of this variable is: {}'.format(type(something)))
print('Is this variable a num ? Answer:{}'.format(something.isnumeric()))
print('Is this variable an alphabetic ? Answer:{}'.format(something.isalpha()))
print('Is this variable an alphanumeric ? Answer:{}'.format(something.isalnum()))
print('Is this variable in uppercase ? Answer:{}'.format(something.isupper()))
print('Is this variable in lowercase ? Answer:{}'.format(something.islower()))
print('Is this variable capitalized ? Answer:{}'.format(something.istitle()))
print('Is this variable a space ? Answer:{}'.format(something.isspace()))




